SELECT HourOfDay, AVG(HourTurnover) as AvgHourTurnover
FROM (
		SELECT HOUR(FARE.PICKUP_TIME) as HourOfDay, SUM(FARE) as HourTurnover
		FROM NYCCAB.FARE
		WHERE WEEKDAY(FARE.PICKUP_TIME) = 1 /* AND HOUR(FARE.PICKUP_TIME) = 19 */
		GROUP BY WEEK(FARE.PICKUP_TIME), HOUR(FARE.PICKUP_TIME))
GROUP BY HourOfDay
ORDER BY HourOfDay